// want to allow to be tags.
// Upon forist request, these will be deciphered.
// This allows to have a list of illicit words without having
// to have them checked into github as a clear text.
// The list will be easily deciphered by any computer programmer
// who is interested.
var decipheredForbiddenTags = null;

export function getDecipheredForbiddenTags() {
    if (!decipheredForbiddenTags)
        decipheredForbiddenTags = forbiddenTagsRot13.map(rot13);
    return decipheredForbiddenTags;
}
// This code taken from Stack Exchange posted by Zibri:
// https://stackoverflow.com/users/236062/zibri
// https://stackoverflow.com/questions/617647/where-is-my-implementation-of-rot13-in-javascript-going-wrong
// (Some modification for TypeScript
function rot13(s)
 {
    return (s ? s : this).split('').map(function(_)
     {
        if (!_.match(/[A-Za-z]/)) return _;
        const c = Math.floor(_.charCodeAt(0) / 97);
        const k = (_.toLowerCase().charCodeAt(0) - 83) % 26 || 26;
        return String.fromCharCode(k + ((c == 0) ? 64 : 96));
     }).join('');
 }

